#Leia dois valores inteiros e compara ambos

x = int(input("Escreva o primeiro valor inteiro: "))
y = int(input("Escreva o segundo valor inteiro: "))

if (x>y):
    print ("O primeiro número é maior que o segundo! ")
if (x<y):
    print ("O segundo valor é maior que o primeiro! ")
else:
    print ("Ambos os números são iguais! ")